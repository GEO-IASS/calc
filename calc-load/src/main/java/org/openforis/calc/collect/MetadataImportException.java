package org.openforis.calc.collect;

/**
 * 
 * @author G. Miceli
 *
 */
public class MetadataImportException extends Exception {

	private static final long serialVersionUID = 1L;

	protected MetadataImportException() {
		super();
	}

	protected MetadataImportException(String message, Throwable cause) {
		super(message, cause);
	}

	protected MetadataImportException(String message) {
		super(message);
	}

	protected MetadataImportException(Throwable cause) {
		super(cause);
	}
}
